    private void btnCekAngkaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekAngkaActionPerformed
         try {
            int angka = Integer.parseInt(txtAngka.getText());

            // Mengecek apakah angka termasuk bilangan GANJIL atau GENAP
            if (angka % 2 == 0) {
                txtGanjilGenap.setText(angka + " Merupakan Bilangan GENAP");
            } else {
                txtGanjilGenap.setText(angka + " Merupakan Bilangan GANJIL");
            }

            // Mengecek apakah angka termasuk bilangan PRIMA
            if (bilPrima(angka)) {
                txtPrima.setText(angka + " Termasuk Bilangan PRIMA");
            } else {
                txtPrima.setText(angka + " Bukan Bilangan PRIMA");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Gagal Menetukan Jenis Bilangan!", "Peringatan",  JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCekAngkaActionPerformed

    private void txtAngkaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAngkaKeyTyped
        char c = evt.getKeyChar();
            if (!Character.isDigit(c) && c != KeyEvent.VK_BACK_SPACE) {
                evt.consume();
                JOptionPane.showMessageDialog(null, "Masukkan Hanya Angka!", "Tidak Valid", 2);
            }
    }//GEN-LAST:event_txtAngkaKeyTyped

